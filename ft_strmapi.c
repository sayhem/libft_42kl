#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t			strlen;
	unsigned int	i;
	char			*str;

	if (!s || !f)
		return (0);
	strlen = ft_strlen(s);
	i = 0;
	str = malloc(sizeof(char) * strlen + 1);
	if (!str)
		return (NULL);
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
